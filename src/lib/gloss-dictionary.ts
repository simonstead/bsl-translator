/**
 * Get all glosses that have videos
 */
export function getAllGlosses(): string[] {
  return Object.keys(GLOSS_TO_VIDEO).filter(
    (gloss) => GLOSS_TO_VIDEO[gloss].videoUrl
  );
}

/**
 * Get glosses organized by category
 */
export function getGlossesByCategory(): Record<string, string[]> {
  const result: Record<string, string[]> = {};
  for (const [category, glosses] of Object.entries(GLOSS_CATEGORIES)) {
    // Only include glosses that have videos
    result[category] = glosses.filter((gloss) => GLOSS_TO_VIDEO[gloss]?.videoUrl);
  }
  return result;
}

/**
 * Get random glosses (only those with videos)
 */
export function getRandomGlosses(count: number): string[] {
  const allGlosses = getAllGlosses();
  const shuffled = [...allGlosses].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

/**
 * Get total count of glosses with videos
 */
export function getGlossCount(): number {
  return getAllGlosses().length;
}
