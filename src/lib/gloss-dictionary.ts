/**
 * Gloss Dictionary
 *
 * Maps English words to BSL glosses and video sources.
 * SignBSL.com is the primary source - videos hosted on media.signbsl.com
 */

export interface SignEntry {
  gloss: string;
  signbslUrl: string;
  videoUrl?: string;
  description?: string;
}

// Helper to construct SignBSL media URLs
// Pattern: https://media.signbsl.com/videos/bsl/{source}/{word}.mp4
function signbslMedia(word: string, source: string = 'signstation'): { videoUrl: string } {
  const base = 'https://media.signbsl.com/videos/bsl';
  return {
    videoUrl: `${base}/${source}/${word}.mp4`,
  };
}

